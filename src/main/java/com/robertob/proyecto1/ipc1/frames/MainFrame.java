    public void updateVehiclesList(){
        this.vehiclesListModel.removeAllElements();
        for (int i = 0; i < 3; i++) {
            if (gameEngine.getPlayer().getVehicleIndex(i) != null) {
                vehiclesListModel.addElement(gameEngine.getPlayer().getVehicleIndex(i).getName());
            }
        }
        this.vehicleList.setModel(vehiclesListModel);
        vehicleCount = this.vehicleList.getModel().getSize();
    }

    public int getVehicleCount() {
        return vehicleCount;
    }
    
    
